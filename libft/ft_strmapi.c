#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	size_t	slen;
	size_t	i;

	slen = ft_strlen(s);
	str = (char *)malloc(sizeof(char) * (slen + 1));
	if (str == NULL)
		return (NULL);
	i = 0;
	while (i < slen)
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
